<h6>How to Weekly Option Calls</h6>
<ol>
	<li>Select <strong>Scan->Stock Scanner</strong></li>
	<li>
		Use the <strong>AS Option CALLS | 38 > 62 EMA 3 Bar Crossover</strong>
		<br>
		Script Download Link: <a href="http://tos.mx/CXgMnO2" target="_blank">http://tos.mx/CXgMnO2</a>
	</li>
	<li>Sort by <strong>Mark %Change (Descending)</strong></li>
	<li>Begin listing with the <strong>Highest Gain Percentages</strong></li>
</ol>
<br>
