				Options are financial instruments that are derivatives based on the value of underlying securities such as stocks. An options contract offers the buyer the opportunity to buy or sell—depending on the type of contract they hold—the underlying asset. Unlike futures, the holder is not required to buy or sell the asset if they choose not to.
				<br>
				<ul>
					<li><strong>Call options</strong> allow the holder to buy the asset at a stated price within a specific timeframe. We purchase these positions when we expected the specified stock price to <strong>increase</strong> over an allotted amount of time.</li>
					<li><strong>Put options</strong> allow the holder to sell the asset at a stated price within a specific timeframe. We purchase these positions when we expected the specified stock price to <strong>decrease</strong> over an allotted amount of time.</li>
				</ul>
