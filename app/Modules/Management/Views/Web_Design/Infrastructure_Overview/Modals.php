				<br>
				<br>
				An <strong>Oversold Bounce</strong> is a rally in prices that occurs due to the selloff preceding it being perceived as too severe. Before an oversold bounve, due to behavioral tendencies such as herding behavior, aversion to loss, and the temptation to panic, prices can decline more than they should based on technical and/or fundamental analysis.
