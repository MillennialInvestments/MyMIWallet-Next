<h6>How to Target Market Movers</h6>
<ol>
	<li>Open the ThinkOrSwim Platform, and visit <strong>MarketWatch</strong></li>
	<li>Select <strong>Quotes</strong></li>
	<li>Change Watchlist to <strong>Lovers and Losers -> Pre-Market Movers</strong></li>
	<li>Sort by <strong>Mark %Change (Descending)</strong></li>
	<li>Begin listing with the <strong>Highest Gain Percentages</strong></li>
</ol>
